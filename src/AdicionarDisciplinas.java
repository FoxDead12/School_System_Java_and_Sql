    public void AdicionarDisciplina(String Nome, String Diminuitivo){
        
        
        if(VerificarSeExisteDisciplina(Nome, Diminuitivo) == true){
            
            DataBaseQuerys baseDados = new DataBaseQuerys();
            Connection conn = baseDados.getDataBaseQuerys();

            try{

                PreparedStatement statement = conn.prepareStatement("INSERT INTO disciplinas (NOME_DISCIPLINA , DIMINUITIVO_DISCIPLINA) VALUES ('"+ Nome +"','"+ Diminuitivo +"');");
                int rows = statement.executeUpdate();
                
                txt_info.setText("Disciplina adicionada com sucesso!");

            }catch(SQLException ex){
                System.out.print(ex);
            }
        }
        else{
            
            txt_info.setText("Esta disciplina j√° existe!");
        }
        
    }
    
    
    public boolean  VerificarSeExisteDisciplina(String Nome, String Diminuitivo){
        
        
            int count = 0;
            DataBaseQuerys baseDados = new DataBaseQuerys();
            Connection conn = baseDados.getDataBaseQuerys();
            try {
                
                PreparedStatement statement = conn.prepareStatement("SELECT COUNT(d.ID_DISCIPLINA) as Count FROM disciplinas d WHERE d.NOME_DISCIPLINA = '"+ Nome +"' OR d.DIMINUITIVO_DISCIPLINA = '"+ Diminuitivo +"'");
                

                ResultSet rs = statement.executeQuery();
                while(rs.next()){
                
                    count = rs.getInt("Count");
                }
                
                
                
                
            }catch (SQLException ex) {
                
            }
            
            if(count == 0){
                    
                    return true;
                }
                else{
                    return false;
                }
    }

